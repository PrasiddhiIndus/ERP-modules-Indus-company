-- ==============================================
-- Trigger: log status and key field changes
-- ==============================================
CREATE OR REPLACE FUNCTION admin_ifsp_employee_master_log_changes()
RETURNS TRIGGER AS $$
BEGIN
    IF OLD.status IS DISTINCT FROM NEW.status THEN
        INSERT INTO admin_ifsp_employee_master_history (master_id, field_name, old_value, new_value, changed_by, change_reason, user_id)
        VALUES (NEW.id, 'status', OLD.status, NEW.status, NEW.updated_by, NEW.status_reason, NEW.user_id);
    END IF;
    IF OLD.full_name IS DISTINCT FROM NEW.full_name THEN
        INSERT INTO admin_ifsp_employee_master_history (master_id, field_name, old_value, new_value, changed_by, user_id)
        VALUES (NEW.id, 'full_name', OLD.full_name, NEW.full_name, NEW.updated_by, NEW.user_id);
    END IF;
    IF OLD.department IS DISTINCT FROM NEW.department THEN
        INSERT INTO admin_ifsp_employee_master_history (master_id, field_name, old_value, new_value, changed_by, user_id)
        VALUES (NEW.id, 'department', OLD.department, NEW.department, NEW.updated_by, NEW.user_id);
    END IF;
    IF OLD.designation IS DISTINCT FROM NEW.designation THEN
        INSERT INTO admin_ifsp_employee_master_history (master_id, field_name, old_value, new_value, changed_by, user_id)
        VALUES (NEW.id, 'designation', OLD.designation, NEW.designation, NEW.updated_by, NEW.user_id);
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS admin_ifsp_employee_master_changes_trigger ON admin_ifsp_employee_master;
CREATE TRIGGER admin_ifsp_employee_master_changes_trigger
    AFTER UPDATE ON admin_ifsp_employee_master
    FOR EACH ROW
    EXECUTE FUNCTION admin_ifsp_employee_master_log_changes();

