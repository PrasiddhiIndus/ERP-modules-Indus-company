-- Vehicle Management System Database Schema
-- For INDUS ERP CORE - Fire Tender/Vehicle Management Module

-- ==============================================
-- Table: vehicles_master
-- Purpose: Master database of all vehicles
-- ==============================================
CREATE TABLE IF NOT EXISTS vehicles_master (
    id bigint generated by default as identity primary key,
    vehicle_type text not null, -- Fire Tender, SUV, Car, Bike, Command Post, etc.
    registration_number text not null unique,
    chassis_number text,
    engine_number text,
    make text not null,
    model text not null,
    year_of_manufacture integer,
    date_of_purchase date,
    date_of_commissioning date,
    assigned_location text,
    assigned_site text,
    assigned_department text,
    assigned_driver_id uuid references auth.users(id),
    vehicle_status text not null default 'Available', -- Available, On Duty, Under Maintenance, Out for Repair, Rented, Decommissioned
    current_odometer_reading numeric default 0,
    last_service_date date,
    next_service_due date,
    remarks text,
    created_at timestamptz default now(),
    updated_at timestamptz default now(),
    user_id uuid references auth.users(id) -- 🔑 link to Supabase Auth user
);

-- Enable Row Level Security
ALTER TABLE vehicles_master ENABLE ROW LEVEL SECURITY;

