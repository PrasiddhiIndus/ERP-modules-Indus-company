-- ==============================================
-- Table: ifsp_employees
-- Purpose: Master database of all IFSPL employees with exact column specifications
-- ==============================================
CREATE TABLE IF NOT EXISTS ifsp_employees (
    id bigint generated by default as identity primary key,
    
    -- Basic Information
    employee_id text not null unique,
    timestamp timestamptz default now(),
    full_name text not null,
    gender text, -- Male, Female, Other
    date_of_joining date not null,
    designation text not null,
    date_of_birth date,
    date_of_anniversary date,
    blood_group text, -- A+, A-, B+, B-, AB+, AB-, O+, O-
    
    -- Identity Documents
    aadhar_no text unique,
    pan_card_no text unique,
    religion text,
    
    -- Family Information
    father_name text,
    mother_name text,
    spouse_name text,
    son_name text,
    son_dob date, -- Son's Date of Birth
    daughter_name text,
    
    -- Contact Information
    address text,
    full_address text,
    personal_no text,
    emergency_no text,
    
    -- Physical & Professional
    identification_mark text,
    years_of_experience numeric, -- Total years of experience
    qualification text,
    attachments text[], -- Array of attachment URLs
    
    -- Reminders
    birthday_reminder boolean default true,
    anniversary_reminder boolean default true,
    
    -- Employment Details
    department text not null,
    other_experience numeric, -- Experience outside IFSPL
    ifspl_experience numeric, -- Experience within IFSPL
    date_of_leaving date, -- DOL - Date of Leaving
    
    -- Status Management
    status text not null default 'Active', -- Active, Inactive
    status_reason text, -- Reason for status change
    status_changed_by text, -- Who changed the status
    status_changed_at timestamptz, -- When status was changed
    
    -- Audit Fields
    created_at timestamptz default now(),
    updated_at timestamptz default now(),
    created_by text,
    updated_by text,
    user_id uuid references auth.users(id) -- 🔑 link to Supabase Auth user
);

-- Enable Row Level Security
ALTER TABLE ifsp_employees ENABLE ROW LEVEL SECURITY;

-- RLS Policies for ifsp_employees
CREATE POLICY "Users can insert their own employees"
    ON ifsp_employees FOR INSERT
    WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Users can view their own employees"
    ON ifsp_employees FOR SELECT
    USING (auth.uid() = user_id);

CREATE POLICY "Users can update their own employees"
    ON ifsp_employees FOR UPDATE
    USING (auth.uid() = user_id);

CREATE POLICY "Users can delete their own employees"
    ON ifsp_employees FOR DELETE
    USING (auth.uid() = user_id);

-- ==============================================
-- Table: ifsp_employee_history
-- Purpose: Track all changes to employee records
-- ==============================================
CREATE TABLE IF NOT EXISTS ifsp_employee_history (
    id bigint generated by default as identity primary key,
    employee_id bigint not null references ifsp_employees(id) on delete cascade,
    field_name text not null,
    old_value text,
    new_value text,
    changed_by text not null,
    changed_at timestamptz default now(),
    change_reason text,
    user_id uuid references auth.users(id)
);

-- Enable Row Level Security for history
ALTER TABLE ifsp_employee_history ENABLE ROW LEVEL SECURITY;

-- RLS Policies for history
CREATE POLICY "Users can view their own employee history"
    ON ifsp_employee_history FOR SELECT
    USING (auth.uid() = user_id);

CREATE POLICY "Users can insert their own employee history"
    ON ifsp_employee_history FOR INSERT
    WITH CHECK (auth.uid() = user_id);

