-- Function to log employee changes
CREATE OR REPLACE FUNCTION log_employee_changes()
RETURNS TRIGGER AS $$
BEGIN
    -- Log changes for each field
    IF OLD.full_name IS DISTINCT FROM NEW.full_name THEN
        INSERT INTO ifsp_employee_history (employee_id, field_name, old_value, new_value, changed_by, user_id)
        VALUES (NEW.id, 'full_name', OLD.full_name, NEW.full_name, NEW.updated_by, NEW.user_id);
    END IF;
    
    IF OLD.status IS DISTINCT FROM NEW.status THEN
        INSERT INTO ifsp_employee_history (employee_id, field_name, old_value, new_value, changed_by, change_reason, user_id)
        VALUES (NEW.id, 'status', OLD.status, NEW.status, NEW.updated_by, NEW.status_reason, NEW.user_id);
    END IF;
    
    IF OLD.department IS DISTINCT FROM NEW.department THEN
        INSERT INTO ifsp_employee_history (employee_id, field_name, old_value, new_value, changed_by, user_id)
        VALUES (NEW.id, 'department', OLD.department, NEW.department, NEW.updated_by, NEW.user_id);
    END IF;
    
    IF OLD.designation IS DISTINCT FROM NEW.designation THEN
        INSERT INTO ifsp_employee_history (employee_id, field_name, old_value, new_value, changed_by, user_id)
        VALUES (NEW.id, 'designation', OLD.designation, NEW.designation, NEW.updated_by, NEW.user_id);
    END IF;
    
    -- Add more field comparisons as needed
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Create trigger for logging changes
CREATE TRIGGER employee_changes_trigger
    AFTER UPDATE ON ifsp_employees
    FOR EACH ROW
    EXECUTE FUNCTION log_employee_changes();

-- Function to get employee count by department
CREATE OR REPLACE FUNCTION get_employee_count_by_department()
RETURNS TABLE (
    department text,
    active_count bigint,
    inactive_count bigint,
    total_count bigint
) AS $$
BEGIN
    RETURN QUERY
    SELECT 
        ie.department,
        COUNT(CASE WHEN ie.status = 'Active' THEN 1 END) as active_count,
        COUNT(CASE WHEN ie.status = 'Inactive' THEN 1 END) as inactive_count,
        COUNT(*) as total_count
    FROM ifsp_employees ie
    WHERE ie.user_id = auth.uid()
    GROUP BY ie.department
    ORDER BY total_count DESC;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Function to get employees with upcoming birthdays
CREATE OR REPLACE FUNCTION get_upcoming_birthdays(days_ahead integer DEFAULT 30)
RETURNS TABLE (
    employee_id text,
    full_name text,
    date_of_birth date,
    days_until_birthday integer
) AS $$
BEGIN
    RETURN QUERY
    SELECT 
        ie.employee_id,
        ie.full_name,
        ie.date_of_birth,
        EXTRACT(DAY FROM (ie.date_of_birth + INTERVAL '1 year' * (EXTRACT(YEAR FROM CURRENT_DATE) - EXTRACT(YEAR FROM ie.date_of_birth)) - CURRENT_DATE))::integer as days_until_birthday
    FROM ifsp_employees ie
    WHERE ie.user_id = auth.uid()
    AND ie.status = 'Active'
    AND ie.birthday_reminder = true
    AND ie.date_of_birth IS NOT NULL
    ORDER BY days_until_birthday;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

