export function filterBySearch(rows, q, keys) {
  if (!q?.trim()) return rows;
  const needle = q.trim().toLowerCase();
  return rows.filter((r) => keys.some((k) => String(r[k] ?? "").toLowerCase().includes(needle)));
}
