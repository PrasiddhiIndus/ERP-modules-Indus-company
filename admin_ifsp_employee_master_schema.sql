-- Table naming: admin_<sub_page> (admin_ifsp_employee_master)

-- ==============================================
-- Table: admin_ifsp_employee_master
-- Purpose: Master list of IFSPL employees (Admin module)
-- ==============================================
CREATE TABLE IF NOT EXISTS admin_ifsp_employee_master (
    id bigint generated by default as identity primary key,

    -- Basic Information
    employee_id text not null,
    full_name text not null,
    gender text,
    date_of_joining date not null,
    designation text not null,
    date_of_birth date,
    date_of_anniversary date,
    blood_group text,

    -- Identity Documents
    aadhar_no text,
    pan_card_no text,
    religion text,

    -- Family Information
    father_name text,
    mother_name text,
    spouse_name text,
    son_name text,
    son_dob date,
    daughter_name text,

    -- Contact Information
    address text,
    full_address text,
    personal_no text,
    emergency_no text,

    -- Physical & Professional
    identification_mark text,
    years_of_experience numeric,
    qualification text,
    attachments text[],

    -- Reminders
    birthday_reminder boolean default true,
    anniversary_reminder boolean default true,

    -- Employment Details
    department text not null,
    other_experience numeric,
    ifspl_experience numeric,
    date_of_leaving date,

    -- Status Management
    status text not null default 'Active',
    status_reason text,
    status_changed_by text,
    status_changed_at timestamptz,

    -- Audit
    created_at timestamptz default now(),
    updated_at timestamptz default now(),
    created_by text,
    updated_by text,
    user_id uuid references auth.users(id)
);

-- Unique constraint on employee_id per tenant (user_id)
CREATE UNIQUE INDEX IF NOT EXISTS idx_admin_ifsp_employee_master_employee_id_user
    ON admin_ifsp_employee_master(user_id, employee_id);

-- Enable Row Level Security
ALTER TABLE admin_ifsp_employee_master ENABLE ROW LEVEL SECURITY;

-- RLS Policies
CREATE POLICY "Users can insert own admin_ifsp_employee_master"
    ON admin_ifsp_employee_master FOR INSERT
    WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Users can view own admin_ifsp_employee_master"
    ON admin_ifsp_employee_master FOR SELECT
    USING (auth.uid() = user_id);

CREATE POLICY "Users can update own admin_ifsp_employee_master"
    ON admin_ifsp_employee_master FOR UPDATE
    USING (auth.uid() = user_id);

CREATE POLICY "Users can delete own admin_ifsp_employee_master"
    ON admin_ifsp_employee_master FOR DELETE
    USING (auth.uid() = user_id);

-- ==============================================
-- Table: admin_ifsp_employee_master_history
-- Purpose: Status and change history for employee records
-- ==============================================
CREATE TABLE IF NOT EXISTS admin_ifsp_employee_master_history (
    id bigint generated by default as identity primary key,
    master_id bigint not null references admin_ifsp_employee_master(id) on delete cascade,
    field_name text not null,
    old_value text,
    new_value text,
    changed_by text,
    changed_at timestamptz default now(),
    change_reason text,
    user_id uuid references auth.users(id)
);

ALTER TABLE admin_ifsp_employee_master_history ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Users can view own admin_ifsp_employee_master_history"
    ON admin_ifsp_employee_master_history FOR SELECT
    USING (auth.uid() = user_id);

CREATE POLICY "Users can insert own admin_ifsp_employee_master_history"
    ON admin_ifsp_employee_master_history FOR INSERT
    WITH CHECK (auth.uid() = user_id);

