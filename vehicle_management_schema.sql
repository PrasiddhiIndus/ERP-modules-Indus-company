-- ==============================================
-- Functions for Alert Management
-- ==============================================

-- Function to update document alert status based on expiry date
CREATE OR REPLACE FUNCTION update_document_alerts()
RETURNS void AS $$
BEGIN
    UPDATE vehicle_documents 
    SET alert_status = CASE 
        WHEN expiry_date < CURRENT_DATE THEN 'Expired'
        WHEN expiry_date <= CURRENT_DATE + INTERVAL '30 days' THEN 'Warning'
        ELSE 'Active'
    END
    WHERE user_id = auth.uid();
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Function to get vehicles with upcoming document expiries
CREATE OR REPLACE FUNCTION get_upcoming_expiries(days_ahead integer DEFAULT 30)
RETURNS TABLE (
    vehicle_id bigint,
    registration_number text,
    document_type text,
    expiry_date date,
    days_remaining integer
) AS $$
BEGIN
    RETURN QUERY
    SELECT 
        vd.vehicle_id,
        vm.registration_number,
        vd.document_type,
        vd.expiry_date,
        (vd.expiry_date - CURRENT_DATE)::integer as days_remaining
    FROM vehicle_documents vd
    JOIN vehicles_master vm ON vd.vehicle_id = vm.id
    WHERE vd.expiry_date <= CURRENT_DATE + (days_ahead || ' days')::interval
    AND vd.user_id = auth.uid()
    ORDER BY vd.expiry_date ASC;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

